#pragma mark - Navigation

// In a story board-based application, you will often want to do a little preparation before navigation
- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    // Get the new view controller using [segue destinationViewController].
    // Pass the selected object to the new view controller.
    
    if ([segue.identifier isEqualToString:@"displayDetailedEvent"]) {
        
        NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];
        PFObject *object = [self.objects objectAtIndex:indexPath.row];
        
        // Set destination view controller to DetailViewController to avoid the NavigationViewController in the middle (if you have it embedded into a navigation controller, if not ignore that part)
        //UINavigationController *nav = [segue destinationViewController];
        //SHScholarshipsDetailedViewController *detailViewController = (SHScholarshipsDetailedViewController *) nav.topViewController;
        SHEventsDetailedViewController *detailViewController = (SHEventsDetailedViewController *)[segue destinationViewController];
        detailViewController.event = object;
    }
    
    if ([segue.identifier isEqualToString:@"displayEventsFavorites"]) {
        
        SHEventsFavoritesTableViewController *eventsFavoritesTableViewController = (SHEventsFavoritesTableViewController *) [segue destinationViewController];
        
        eventsFavoritesTableViewController.favoritesList = _favoritesList;
    }
    
}

